<p align="center">Orientações</p>
		<ul>
<li>A portaria N. 2394/GR/IFAL de 07 de Outubro de 2015 é o documento do IFAL que rege as Atividades Complementares.</li>

<li>As atividades complementares correspondem a 200 horas que podem ser cumprida pelo aluno durante todo o curso de graduação. (Art. 2º, Portaria 2394/GR/IFAL)</li>

<li>Cada aluno deve escolher as atividades que deseja participar. (Art 3º, Portaria 2394/GR/IFAL)</li>

<li>O aluno deve realizar pelo menos 3 itens de atividades complementares definidas no Art. 4º. (Portaria 2394/GR/IFAL)</li>

<li>Procedimentos (Art 5º, Portaria 2394/GR/IFAL)</li>

<li>Aluno cadastra atividades complementares no sistema e anexa comprovantes.</li>

<li>Coordenador valida ou rejeita atividades cadastradas.</li>

<li>A carga horária para cada atividade, bem como os limites são definidos Art 5º §3 (, Portaria 2394/GR/IFAL) na PROEX;</li>

